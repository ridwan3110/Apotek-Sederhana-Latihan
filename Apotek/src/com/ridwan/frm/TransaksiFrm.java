    private void btn_hitungtotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hitungtotalActionPerformed
    int jumlahbaris = tabel_trx.getRowCount();
    double total_biaya = 0;
    double jumlahBarang, hargaBarang;
        TableModel tableModel;
        tableModel = tabel_trx.getModel();
        if (validasiHitung()==0){
            JOptionPane.showMessageDialog(null, "Masih ada barang yang Jumlahnya 0");
        }else {
            for (int i=0; i<jumlahbaris; i++){
               jumlahBarang = Double.parseDouble(tableModel.getValueAt(i, 1).toString());
               hargaBarang = Double.parseDouble(tableModel.getValueAt(i, 2).toString());
               total_biaya =total_biaya + (jumlahBarang*hargaBarang);
            }
            txt_total.setText(String.valueOf(total_biaya));
        }
    }//GEN-LAST:event_btn_hitungtotalActionPerformed

