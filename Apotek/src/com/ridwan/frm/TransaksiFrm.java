    private boolean validasiInput(){
        boolean valid = false;
        if (txt_kodetrx.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Transaksi Kode Belum Ada");
        }else if (txt_tgltrx.getDate()==null){
            JOptionPane.showMessageDialog(null, "Tanggal Transkasi belum ada");
        }else if(tabel_trx.getRowCount()==0){
            JOptionPane.showMessageDialog(null, "obat belum dipilih!");
        }else if (cbo_petugas.getSelectedIndex()==0){
            JOptionPane.showMessageDialog(null, "petugas belum dipilih");
        } else if (txt_total.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "TOTAL BELUM ADA");
        }
        else {
           valid = true;
        }
        return valid;
    }
    
    
    private int validasiHitung(){
        int jumlah = 0;
        for (int i=0; i<tableModelTransaksiDetail.getRowCount(); i++){
            jumlah = (int) tabel_trx.getValueAt(i, 1);
        }
        return jumlah;
    }

