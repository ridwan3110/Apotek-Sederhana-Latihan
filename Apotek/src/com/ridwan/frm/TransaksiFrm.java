    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    if (validasiInput()==true){
        transaksi = new Transaksi();
        transaksi.setTransaksikd(String.valueOf(txt_kodetrx.getText()));
        transaksi.setTglTransaksi(txt_tgltrx.getDate());
      transaksi.setPetugas((PetugasModel) cbo_petugas.getSelectedItem());
        
        List<TransaksiDetail> transaksiDetils = new ArrayList<>();
        for (int i=0; i<tabel_trx.getRowCount();  i++){
            TransaksiDetail detil = new TransaksiDetail();
            detil.setTransaksi(transaksi);
            
            ObatModel om = (ObatModel) tabel_trx.getValueAt(i, 0);
            int jumlah = (int) tabel_trx.getValueAt(i, 1);
            Double harga = (Double) tabel_trx.getValueAt(i, 2);
            
            detil.setHarga(harga);
            detil.setJumlah(jumlah);
            detil.setObat(om);
            transaksiDetils.add(detil);
        }
        
        transaksi.setGettrTransaksiDetils(transaksiDetils);
        if (transaksiDao.InsertTransaksi(transaksi)==true){
            JOptionPane.showMessageDialog(null,"Data Berhasil Disimpan");
            list.add(transaksi);
            resetFrm();
            try {
            TransaksiInterface dao = new TransaksiDao();
            List<ReportTransaksi> transaksi1 = dao.getTransaksi(transaksi);
            HashMap map = new HashMap();
            map.put("SubTitle", "Berdasarkan ID Transaksi"+transaksi.getTransaksikd());
            JasperPrint jp = JasperFillManager.fillReport(this.getClass().getClassLoader().getResourceAsStream
                ("com/ridwan/Laporan/LaporanTransaksi.jasper"), map, new JRBeanCollectionDataSource(transaksi1));
            JRViewer jrv = new JRViewer(jp);
            TampilReport report = new TampilReport("Laporan Transaksi berdasarkan Id", jrv);
        } catch (JRException ex) {
            Logger.getLogger(TransaksiFrm.class.getName()).log(Level.SEVERE, null, ex);
        }      
            
        }else {
            JOptionPane.showMessageDialog(null, "Data gagal disimpan");
            }
        
    }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void btn_ambilobatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ambilobatActionPerformed
     boolean valid = false;
        Ambilobatdialog ambilobatdialog = new Ambilobatdialog();
        obatModel = ambilobatdialog.ambilObat();
        if (obatModel!=null){
            List<ObatModel> obatcek = ambilobatdialog.tableModelObat.getObatCek();
            for (ObatModel om : obatcek){
                TransaksiDetail detil = new TransaksiDetail();
                detil.setObat(om);
                detil.setJumlah(0);
                detil.setHarga(om.getHarga());
                
                for (int i=0 ; i<tabel_trx.getRowCount(); i++){
                    TransaksiDetail td = tableModelTransaksiDetail.getTransaksiDetail(i);
                    if (td.getObat().getNamaObat().equals(detil.getObat().getNamaObat())){
                        valid = true;
                    }
                }
                if (valid==false){
                    tableModelTransaksiDetail.insertTransaksiDetail(detil);
                }else {
                    JOptionPane.showMessageDialog(null, "MAaf Nama Barang Sudah ada");
                }
            }
        }
        
    }//GEN-LAST:event_btn_ambilobatActionPerformed

