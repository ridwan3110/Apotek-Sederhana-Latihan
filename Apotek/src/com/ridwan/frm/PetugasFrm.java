    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       if (validasiInput()==true){
           String id = txt_idpetugas.getText();
           String nama = txt_namapetugas.getText();
           String username = txt_username.getText();
           char [] pwd = txt_password.getPassword();
           String password = new String(pwd);
           String Status = (String) cbo_status.getSelectedItem();
           String alamat = txt_alamat.getText();
           String telepon = txt_telepon.getText();
           
           PetugasModel p = new PetugasModel();
           p.setPetugasId(id);
           p.setNama(nama);
           p.setUsername(username);
           p.setPassword(password);
           p.setStatus(Status);
           p.setAlamat(alamat);
           p.setTelepon(telepon);
           
           petugasDao.insertPetugas(p);
           tableModelPetugas.insertPetugas(p);
           JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
           loadData();
           resetFrm();
       }
    }//GEN-LAST:event_jButton1ActionPerformed

