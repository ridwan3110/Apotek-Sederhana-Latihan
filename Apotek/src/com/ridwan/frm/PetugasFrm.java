    private boolean validasiInput(){
        boolean valid = false;
        if (txt_idpetugas.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "ID Petugas Tidak Boleh Kosong");
        }else if (txt_namapetugas.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Nama Petugas Tidak BOleh Kosong");
        }else if (txt_username.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Usernam tidak boleh kosong");
        }else if (txt_password.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Password tidak boleh kosong");
        }else if (cbo_status.getSelectedIndex()==0){
            JOptionPane.showMessageDialog(null,"Status belum dipilih");
        }else if (txt_alamat.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null,"Alamat Masih Kosong");
        }else if (txt_telepon.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Telepon harus diisi");
        }
        else {
            valid = true;
        }
        return valid;
    }

