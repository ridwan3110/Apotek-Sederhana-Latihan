    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
         List<PetugasModel> Cekpetugas = tableModelPetugas.getpetugasCek();
if (!Cekpetugas.isEmpty()){
    if (JOptionPane.showConfirmDialog(null, "Konfirmasi", "Apakah Anda ingin menghapus Data ini ?", 
            JOptionPane.OK_CANCEL_OPTION)==JOptionPane.OK_OPTION){
        boolean valid = true;
        for (PetugasModel petugasModel : Cekpetugas){
            if (valid){
                petugasDao.deletePetugas(petugasModel);
            }
             loadData() ;
    resetFrm();
        }
        JOptionPane.showMessageDialog(null, "Data Berhasil Dihapus");
       
    }
   
    
}else {
    JOptionPane.showMessageDialog(null,"Seleksi salah satu Baris");
}
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
int index = tabel_petugas.getSelectedRow();
if (index!=-1){
    PetugasModel model = tableModelPetugas.getPetugasModel(tabel_petugas.convertRowIndexToModel(index));
    Dialog_update_petugas d = new Dialog_update_petugas();
    PetugasModel pm = d.updatePetugas(model);
    if (pm!=null){
        petugasDao.updatePetugas(model);
        tableModelPetugas.updatePetugas(index, model);
    }
}else {
    JOptionPane.showMessageDialog(null, "Data Berhasil diupdate");
}
    }//GEN-LAST:event_jButton2ActionPerformed

