    private void Saring(){
        String text = txt_cariobat.getText();
        if (text.length()==0){
            rowSorter.setRowFilter(null);
        }else {
           rowSorter.setRowFilter(RowFilter.regexFilter
        (Pattern.compile("(?i).*" + text + ".*", Pattern.CASE_INSENSITIVE|Pattern.DOTALL ).toString()));
        }
    }
    
    
    private void loadData(){
        List<GudangModel> gudangModels = gudangDao.getgudangModels();
        tableModelGudang.setData(gudangModels);
    }
    
    private void loadGudang(){
        List<GudangModel> gudangModels = gudangDao.getgudangModels();
        for (GudangModel g : gudangModels){
            cbo_gudang.addItem(g);
        }
    }
    
    
    private boolean validasiInput(){
        boolean valid = false;
        if (txt_kodegudang.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Kode Gudang Masih Kosong");
        }else if (txt_namagudang.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Nama Gudang Masih Kosong");
        }else if (txt_namaobat.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Nama Obat Belum Diiisi");
        }else if (txt_jumlahstok.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Stok Obat Belum Diisi");
        }else {
            valid = true;
        }
        return valid;
    }
    
    
