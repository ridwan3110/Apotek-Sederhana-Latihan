    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
List<GudangModel> cekgudang = tableModelGudang.getGudangCek();
if (!cekgudang.isEmpty()){
if (JOptionPane.showConfirmDialog(null,"Konfirmasi", "Apakah anda ingin menghapus data ini ?",
        JOptionPane.OK_CANCEL_OPTION)==JOptionPane.OK_OPTION){
    boolean valid = true;
    for (GudangModel g : cekgudang){
        if (valid){
            gudangDao.deleteGudang(g);
            loadData();
            loadGudang();
        }
    }
    JOptionPane.showMessageDialog(null, "Data Berhasil DIhapus");
    loadData();
    loadGudang();
    
}
}
else {
    JOptionPane.showMessageDialog(null, "Seleksi Salah Satu Baris");
}
loadGudang();
    }//GEN-LAST:event_btn_deleteActionPerformed

    private void btn_carigudangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_carigudangActionPerformed
      if(cbo_gudang.getSelectedIndex()==0){
            JOptionPane.showMessageDialog(null, "Siilahkan pilih Gudang Obat");
        }else{
            
          GudangModel gm = (GudangModel) cbo_gudang.getSelectedItem();
          List<GudangModel> gudangModels = gudangDao.getgudangmodelid(gm);
            
            if(!gudangModels.isEmpty()){
                tableModelGudang.setData(gudangModels);
            }else{
                JOptionPane.showMessageDialog(null, "Data Gudang tidak ditemukan");
            }
        }
        
    }//GEN-LAST:event_btn_carigudangActionPerformed

    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
      int index = tabel_gudang.getSelectedRow();
      if (index!=-1){
          GudangModel g =  tableModelGudang.getGudangModel(tabel_gudang.convertRowIndexToModel(index));
          Dialog_update_gudang d = new Dialog_update_gudang();
          GudangModel update = d.updateGudang(g);
          if (update!=null){
              gudangDao.updateGudang(update);
              tableModelGudang.updateGudang(index, update);
          }
      }else {
          JOptionPane.showMessageDialog(null, "Seleksi Salah Satu Baris");
      }
    }//GEN-LAST:event_btn_updateActionPerformed

