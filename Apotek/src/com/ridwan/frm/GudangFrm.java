    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
       if (validasiInput()==true){
           String kode = txt_kodegudang.getText();
           String namagudang = txt_namagudang.getText();
           String Namaobat = txt_namaobat.getText();
           int jumlahstok = Integer.valueOf(txt_jumlahstok.getText());
           
           GudangModel gm = new GudangModel();
           gm.setGudangkd(kode);
           gm.setNamaGudang(namagudang);
           gm.setNamaObat(Namaobat);
           gm.setStokobat(jumlahstok);
           
           gudangDao.insertGudang(gm);
           tableModelGudang.insertGudang(gm);
           loadData();
           resetFrm();
           JOptionPane.showMessageDialog(null, "Data berhasil Disimpan");
           loadGudang();
           loadGudang();
       }
    }//GEN-LAST:event_btn_simpanActionPerformed

